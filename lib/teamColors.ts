/**
 * Returns the primary hex colour for a team name.
 * Matching is case-insensitive partial match.
 * @param name Team name (any casing)
 * @returns Hex colour string, e.g. '#EF0107', or '#888888' as fallback
 */
export function getTeamColor(name: string): string {
  const lower = name.toLowerCase();
  for (const [key, color] of TEAM_COLORS) {
    if (lower.includes(key)) return color;
  }
  return '#888888';
}
